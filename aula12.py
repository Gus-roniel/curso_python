# OPERADORES DE ATRIBUIÇÃO
# =
a = 12
print(a)

# +=
b = 5
b += 12
print(b)
c = 1
c = c + 5
print(c)

# -=
b = 5
b -= 12
# b = 5 - 12
print(b)

# *=
b = 5
b *= 2
print(b)


# /=
b1 = 50.0
b1 /= 10
print(b1)

# %=
b = 5
b %= 2
# b = b % 2
print(b)

# OPERADORES LÓGICOS
# and
print(2 == 2 and 15 > 7)

# or
print(2 != 2 or 5 > 7)

# not
a = False
print(a)
print(not (a))

# OPERADORES DE ASSOCIAÇÃO
frase = 'Estudar Python é muito legal'
palavra = "muito"

# in
print(palavra in frase)

# not in
print(palavra not in frase)

# OPERADORES DE
# =
a = 12
print(a)

# +=
b = 5
b += 12
print(b)

# -=
b = 5
b -= 12
print(b)

# *=
b = 5
b *= 2
print(b)


# /=
b1 = 50
b1 /= 10
print(b1)

# %=
b = 5
b %= 2
print(b)

# OPERADORES LÓGICOS
# and
print(2 == 2 and 5 > 7)

# or
print(2 == 2 or 5 > 7)

# not
a = False
print(not (a))

# OPERADORES DE ASSOCIAÇÃO
frase = 'Estudar Python é muito legal'
palavra = "muito"

# in
print(palavra in frase)

# not in
print(palavra not in frase)
